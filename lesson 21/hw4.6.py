# Написать программу, которая определяет представляет ли строка двоичное число.

# Программа получает из stdin строку, содержащую один из нескольких возможных форматов строкового представления двоичного числа: без префикса, префикс 'b', префикс '0b'. 
# Например:
#     0101
#     b11
#     0b11001

# Программа выводит в stdout текстовый ответ.

# В этой задаче необходимо использовать множества.

# Пример ввода:
#     1b0101

# Пример вывода:
#     нет

def is_binary_string(s: str) -> bool:
    # Множество допустимых символов для двоичного числа
    allowed_int = {'0', '1'}

    # Проверяем, что все символы, кроме префиксов, двоичные
    for char in s:
        if char not in allowed_int and char not in {'b', '0', '1'}:
            return False
    
    # Проверяем префиксы
    if s.startswith('0b') or s.startswith('b'):
        s = s[2:] if s.startswith('0b') else s[1:]  # Отбрасываем префикс

    # Проверяем, что все остальные символы двоичные
    for char in s:
        if char not in allowed_int:
            return False
    
    return True


# Ввод пользователя
binary_string = input('Введите двоичное число: ').strip()

# Выводим результат
if is_binary_string(binary_string):
    print('Да')
else:
    print('Нет')

